# Leia quanto dinheiro uma pessoa tem na carteira e mostre quantos dolares ela pode comprar
# Considere o dolar valendo R$3,27

din = float(input("Digite quanto dinheiro você tem: R$ "))
dolar = din / 3.27

print("Com os R$ {} que você tem na carteira, você pode comprar $ {:.2f}".format(din,dolar))






